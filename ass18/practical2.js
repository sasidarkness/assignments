let color = prompt("Enter a color:", "red").toLowerCase();

switch (color) {
  case "red":
    alert("Red is the color of energy and passion!");
    break;
  case "blue":
    alert("Blue is so calm and peaceful!");
    break;
  case "green":
    alert("Green is the color of nature and freshness!");
    break;
  case "yellow":
    alert("Yellow is bright and full of happiness!");
    break;
  case "black":
    alert("Black looks elegant and powerful!");
    break;
  default:
    alert("That's a nice color!");
}

